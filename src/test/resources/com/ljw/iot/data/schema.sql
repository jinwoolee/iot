CREATE TABLE SENSOR (
	SENSOR_ID BIGINT NOT NULL,
	SENSOR_TYPE		VARCHAR(20),		--센서구분(DUST : 먼지센서)
	SENSOR_NAME VARCHAR(50),
	DESC VARCHAR(50),
	REG_DT DATETIME,
	PRIMARY KEY (SENSOR_ID)
);

CREATE TABLE MEASURE (
	MEASURE_ID BIGINT IDENTITY NOT NULL PRIMARY KEY,
	SENSOR_ID BIGINT NOT NULL,
	MEASURE DOUBLE,
	REG_DT DATETIME
);